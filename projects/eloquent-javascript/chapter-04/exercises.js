function reverseArray(array) {
  // create a array storage
  var arrayReverse = [];
  // create a loop
  for (var i = 0; i < array.length; i++){
    // unshift the elements 
    arrayReverse.unshift(array[i]);
    // return the arrayReverse
  }return arrayReverse;

}

