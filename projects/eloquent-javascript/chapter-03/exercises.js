// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
    // create a counter system
    let counter = 0;
    // convert string into an array of individual letters
    let stringCount = string.split("");
    // create a loop to loop through the array
    for (var i = 0; i < stringCount.length; i++){
        // conditional where the strings at the i index is equal to a char letter
        if( string[i] === char){
            // then increment the counter by 1
            counter++;
        }
        // outside of the loop return the counter
    } return counter;

}

