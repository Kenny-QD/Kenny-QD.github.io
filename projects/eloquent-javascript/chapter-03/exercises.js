////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
    // create a conditional 
    if( num1 < num2){
        // if num1 is less than num2 return num1
        return num1
    } else 
    // everything else, then return num2
    return num2
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
// create a recursive problem
// basecase if the num is 0
if (num === 0){
    // return true
    return true;
    // if the number has a 1 remainder if it is false
} else if (num === 1){
    return false;
    // if the number is greater than 0
} else if (num > 0){
    // recursively subtract the number by 2
    return isEven(num -2);
} else {
    // the the number is negative multiply it by a negative
    return isEven(-(num -2));
}
}

